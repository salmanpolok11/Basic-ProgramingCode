// ** While Loop Lekhar Niyom
var count = 0;
while (count < 15) {
   console.log("Hello World");
   count++
}


// **While loop diye bejor songkha ber korar niyom =>
let scour = 1
while (scour <= 20) {
   console.log(scour);
   scour += 2;
}


// **While loop diye jor songkha ber korar niyom =>
let rostGiven = 0;
while (rostGiven <= 10) {
   console.log("Hello World");
   console.log(rostGiven);
   rostGiven = rostGiven + 2;
}


